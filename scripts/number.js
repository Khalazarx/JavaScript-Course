// make a number binary
document.writeln("<h3>Make a number with binary</h3>");
document.writeln( 0b101010 ); // binary
document.writeln( "<br>" );

// make a number octal
document.writeln("<h3>Make a number with octal</h3>");
document.writeln( 0o777 ); // octal
document.writeln( "<br>" );

// make a number hexadecimal
document.writeln("<h3>Make a number with hexadecimal</h3>");
document.writeln( 0xFF ); // hexadecimal
document.writeln( "<br>" );

// make a number with underscores
document.writeln("<h3>Make a number with underscores</h3>");
document.writeln( 1_000_000 ); // one million
document.writeln( "<br>" );
document.writeln( 1_000_000_000 ); // one billion
document.writeln( "<br>" );
document.writeln(1_000_000_000); // one billion